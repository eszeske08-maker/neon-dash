    // Detect browser language
    detectLanguage() {
        const browserLang = navigator.language.split('-')[0];
        const supported = Object.keys(this.translations);
        this.currentLang = supported.includes(browserLang) ? browserLang : 'en';
        console.log(`[i18n] Detected language: ${navigator.language}, using: ${this.currentLang}`);
        return this.currentLang;
    },

    // Get translation
    t(key, params = {}) {
        let text = this.translations[this.currentLang]?.[key]
            || this.translations.en[key]
            || key;

        // Replace {param} placeholders
        Object.keys(params).forEach(k => {
            text = text.replace(`{${k}}`, params[k]);
        });

        return text;
    },

    // Translate all elements with data-i18n attribute
    translatePage() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            el.textContent = this.t(key);
        });

        // Handle placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });
    },

